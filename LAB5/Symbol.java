
public class Symbol {
	private String symbol;
	private int place;
	public Symbol(String symbol, int place) {
		this.symbol = symbol;
		this.place = place;
	}

	public String getSymbol() {
		return symbol;
	}

	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}

	public int getPlace() {
		return place;
	}

	public void setPlace(int place) {
		this.place = place;
	}
}
